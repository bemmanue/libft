#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*buf;

	buf = b;
	if (len != 0)
	{
		while (len != 0)
		{
			*buf++ = c;
			len--;
		}
	}
	return (b);
}
